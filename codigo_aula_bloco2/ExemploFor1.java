package codigo_aula_bloco2;
public class ExemploFor1 {

    public static void main(String[] args) {
        // exibir os numeros de 1 a 1000 na tela
        for (int cont=1; cont<=1000; cont++) { // for(inicio; teste; passo)
        // se já declarou o início em outro lugar, pode omitir na linha do for.
            System.out.println(cont + " ");
        }
    }  
}

